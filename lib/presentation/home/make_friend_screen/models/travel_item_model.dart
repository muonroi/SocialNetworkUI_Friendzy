class TravelItemModel {
  TravelItemModel({
    required this.postTitle,
    required this.postImage,
    required this.author,
    required this.address,
    required this.icon,
    required this.id,
  }) {
    postTitle = postTitle;
    author = author;
    address = address;
    icon = icon;
    id = id;
    postImage = postImage;
  }
  String postTitle;
  String postImage;
  String author;
  String address;
  String icon;
  String id;
}
